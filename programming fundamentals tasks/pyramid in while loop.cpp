#include <iostream>
using namespace std;
int main()
{
    int i = 0, j = 0, k = 0, space = 60;
    
    while(i<=5)
     i++;
    
    {
        while(k<space)
       k++;
        {
         cout<<" ";
          
        }
         while(j<2*i-1)
         j++;
        {
                cout<<"*";
                
               
        }
       

        space--;
        cout<<"\n";
        
    }
     
     
    return 0;
    
}

